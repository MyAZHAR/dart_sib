void main(){
  //operator
  var prefixIncrement = 5;
  print(++prefixIncrement);

  var positifIncrement = 5;
  print(positifIncrement++);
  print(positifIncrement);

  var prefixDecrement = 5;
  print(--prefixDecrement);

  var negativeDecrement = 5;
  print(negativeDecrement--);
  print(negativeDecrement);
}
